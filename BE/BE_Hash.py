class Hash(object):
    __nombre = ""
    __tipo = ""
    __ruta = ""
    __descripcion = ""

    def getNombre(self):
        return self.nombre

    def setNombre(self, nombre):
        self.nombre = nombre

    def getTipo(self):
        return self.tipo

    def setTipo(self, tipo):
        self.tipo = tipo

    def getRuta(self):
        return self.ruta

    def setRuta(self, ruta):
        self.ruta = ruta

    def getDescripcion(self):
        return self.descripcion

    def setDescripcion(self, descripcion):
        self.descripcion = descripcion






