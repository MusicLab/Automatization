class Archivo(object):
    __ruta = ""
    __fecha = ""
    __nombre = ""

    def getRuta(self):
        return self.ruta

    def setRuta(self, ruta):
        self.ruta = ruta

    def getFecha(self):
        return self.fecha

    def setFecha(self, fecha):
        self.fecha = fecha

    def getNombre(self):
        return self.nombre

    def setNombre(self, nombre):
        self.nombre = nombre




